

public class Expression {

	public static void main(String[] args) {
		int friends = 10;
		
		friends = friends +1 ;
		System.out.println(friends);
		friends ++;
		System.out.println(friends);
		
        int friend = 10;
		
		friend = friend -1 ;
		System.out.println(friend);
		friend --;
		System.out.println(friend);
		
		int x = 10;
		
		x= x/3;
		System.out.println(x);
		
		double X= 10;
		X= X/3;
		System.out.println(X);
	}

}
